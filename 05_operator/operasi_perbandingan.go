package main

import "fmt"

func main() {
	// operasi perbandingan
	// >  lebih dari
	// <  kurang dari
	// >= lebih dari sama dengan
	// <= kurang dari sama dengan
	// == sama dengan
	// != tidak sama dengan

	var nilai1 = 5
	var nilai2 = 3

	fmt.Println(nilai1 > nilai2)
	fmt.Println(nilai1 >= nilai2)
	fmt.Println(nilai1 < nilai2)
	fmt.Println(nilai1 <= nilai2)
	fmt.Println(nilai1 == nilai2)
	fmt.Println(nilai1 != nilai2)

     fmt.Println('\n')

     nama1 := "budi"
     nama2 := "budi"
     
     fmt.Println(nama1 == nama2)
}